package com.java.ashokIT;

public class FindTheSumOfNumberUsing {


    public static void main(String[] args) {
        System.out.println("Pls Find the sum of number started from 1 to 50");
        int sum=0;
        for(int i=1; i<=50; i++){
            sum =sum+i;
        }
        System.out.println(sum);
        /*using Formula  */


    }
}
